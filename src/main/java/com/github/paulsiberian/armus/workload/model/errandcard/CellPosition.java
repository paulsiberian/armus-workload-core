package com.github.paulsiberian.armus.workload.model.errandcard;

public class CellPosition {

    private int i;
    private int j;

    CellPosition(int i, int j) {
        this.i = i;
        this.j = j;
    }

    public int getI() {
        return i;
    }

    public int getJ() {
        return j;
    }
    
}
